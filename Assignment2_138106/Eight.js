var a = Number(x);

if (a>0) {
    document.write("The provided Number is " + " " + a + " and is a POSITIVE Number");
}
if (a<0) {
    document.write("The provided Number is " + " " + a + " and is a NEGATIVE Number");
}
if (a==0) {
    document.write("The provided Number is ZERO");
}

 